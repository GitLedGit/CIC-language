print("CIC language: Write code: ")
while True:
  c = input()
  if c == "10101":
    print("on off on off on")
  if c == "01010":
    print("off on off on off")
  if c == "10101#01010":
    print("on off on off on # off on off on off")
  if c == "01010#10101":
    print("off on off on off # on off on off on")

  if c == "11011":
    print("on on off on on")
  if c == "00100":
    print("off off on off off")